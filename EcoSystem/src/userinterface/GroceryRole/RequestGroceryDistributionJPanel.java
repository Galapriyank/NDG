    private void submitRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitRequestActionPerformed
        // TODO add your handling code here:
        if (eventDate.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Please select a valid date for event!!!");
            return;
        }
        if (messageText.getText().equals("") || messageText.getText() == null) {
            JOptionPane.showMessageDialog(null, "Message cannot be blank!!!");
            return;
        }
        if (tblItems.getRowCount() < 1) {
            JOptionPane.showMessageDialog(null, "Atleast one item should be added!");
            return;
        }
        if(ngoNameComboBox.getSelectedItem() == null){
            JOptionPane.showMessageDialog(null, "Organization name not selected!");
            return;
        }
        GroceryDistributionEventRequest request = new GroceryDistributionEventRequest();
        Date date = eventDate.getDate();
        String message = messageText.getText();

        request.setDate(date);
        request.setMessage(message);
        request.setSender(userAccount);
        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (organization instanceof NGOAdminOrganization) {
                org = organization;
                request.setStatus("Pending with Grocery Admin");
                break;
            } else if (organization instanceof GroceryAdminOrganization) {
                org = organization;
                request.setStatus("Pending with NGO Admin");
                break;
            }
        }
        if (org != null) {
            org.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
        }
        JOptionPane.showMessageDialog(null, "Grocery Distribution Request created successfully!.!");
        itemNameTxt.setText("");
        messageText.setText("");
        itemQtyTxt.setText("");

    }//GEN-LAST:event_submitRequestActionPerformed

    public void populateOrganizationComboBox() {
        ngoNameComboBox.removeAllItems();
        for (Network network : business.getNetworkList()) {
            for (Enterprise enter : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization organization : enter.getOrganizationDirectory().getOrganizationList()) {
                    for (UserAccount ua : organization.getUserAccountDirectory().getUserAccountList()) {
                        if (ua.equals(userAccount)) {
                            if (ua.getRole() instanceof GroceryRole) {
                                for (Enterprise enter1 : network.getEnterpriseDirectory().getEnterpriseList()) {
                                    for (Organization org : enter1.getOrganizationDirectory().getOrganizationList()) {
                                        if (org instanceof NGOAdminOrganization) {
                                            ngoNameComboBox.addItem(enter1.getName());
                                        }
                                    }
                                }
                            } else if (ua.getRole() instanceof NGORole) {
                                for (Enterprise enter1 : network.getEnterpriseDirectory().getEnterpriseList()) {
                                    for (Organization org : enter1.getOrganizationDirectory().getOrganizationList()) {
                                        if (org instanceof GroceryAdminOrganization) {
                                            ngoNameComboBox.addItem(enter1.getName());
                                        }

                                    }
                                }

                            }
                        }
                    }
                }
            }
        }
    }

    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
        Component[] comps = userProcessContainer.getComponents();
        for(Component c : comps){
            if (c instanceof NGOWorkAreaJPanel){
                NGOWorkAreaJPanel ngoPanel = (NGOWorkAreaJPanel) c;
                ngoPanel.populateTable();
            } else if (c instanceof GroceryStoreWorkAreaJPanel){
                GroceryStoreWorkAreaJPanel panel = (GroceryStoreWorkAreaJPanel) c;
                panel.populateTable();
            }   
        }
    }//GEN-LAST:event_backJButtonActionPerformed

