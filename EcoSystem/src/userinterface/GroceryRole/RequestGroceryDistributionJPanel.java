    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here
        if (itemNameTxt.getText() == null || itemNameTxt.getText().equals("") || itemQtyTxt.getText().equals("") || itemQtyTxt.getText() == null
                || expDate.getDate() == null) {
            JOptionPane.showMessageDialog(null, "One or more fields Invalid!!!");
            return;
        }
        if (expDate.getDate().getTime() < eventDate.getDate().getTime()) {
            JOptionPane.showMessageDialog(null, "Product expiry date should be atleast a month after event date!");
            return;
        }
        if (!itemQtyTxt.getText().matches("[0-9]{1,5}")){
            JOptionPane.showMessageDialog(null, "Item quantity should be numeric!");
            return;
        }
        if (!itemNameTxt.getText().matches("[a-zA-Z]*")){
            JOptionPane.showMessageDialog(null, "Item name cannot be numeric!");
            return;
        }
        String iName = itemNameTxt.getText();
        int qty = Integer.parseInt(itemQtyTxt.getText());
        Date date = expDate.getDate();
        Items items = new Items();
        items.setItemName(iName);
        items.setQty(qty);
        items.setExpDate(date);
        itemList.add(items);
        populateTable(itemList);
    }//GEN-LAST:event_AddButtonActionPerformed

