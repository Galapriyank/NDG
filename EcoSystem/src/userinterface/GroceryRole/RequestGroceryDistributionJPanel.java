    private void populateTable(List<Items> itemList) {
        DefaultTableModel model = (DefaultTableModel) tblItems.getModel();
        model.setRowCount(0);
        for (Items item : itemList) {
            Object[] row = new Object[3];
            row[0] = item;
            row[1] = item.getQty();
            row[2] = item.getExpDate();
            model.addRow(row);
        }
    }
